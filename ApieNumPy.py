# NumPy yra galinga biblioteka, kuri teikia daugybę funkcijų ir veiksmų,
# skirtų įvairioms skaičiavimo ir duomenų manipuliavimo užduotims.


# Masyvų matematiniai veiksmai:



# np.sin, np.cos, np.tan - sinusas, kosinusas, tangentas
# np.exp - eksponentas
# np.log, np.log10 - natūralusis algoritmas, dešimtainis 
# algoritmas
# np.sqrt - kvadratinė šaknis
# np.sum, np.mean, np.std - suma, vidurkis, standartinis nuokrypis

# Masyvų manipuliavimas:

# np.reshape - keičia masyvo formą (išdėstymą)
# np.concatenate - sujungia masyvus
# np.split - skaido masyvą į dalis
# np.sort - rikiuoja masyvą
# np.unique - grąžina unikalius masyvo elementus

# Masyvų algebra ir statistika:

# np.dot - matricų daugyba
# np.linalg.inv - atvirkštinė matrica
# np.linalg.solve - sprendžia lygčių sistemą
# np.cov - kovariacijos matrica
# np.corrcoef - koreliacijos matrica

# Duomenų filtravimas ir sąlygų taikymas:
  
# np.where - grąžina indeksus, kurie tenkina sąlygą
# np.logical_and, np.logical_or, np.logical_not - loginiai operatoriai

# Duomenų atvaizdavimas ir vizualizacija su NumPy ir Matplotlib:

# np.arange - sugeneruoja skaičių seką
# np.linspace - sugeneruoja lygiai pasiskirstytus skaičius tarp intervalo
# np.random - generuoja atsitiktinius skaičius
# plt.plot - braižo linijinę diagramą
# plt.scatter - braižo taškų diagramą
# plt.hist - braižo histogramą
